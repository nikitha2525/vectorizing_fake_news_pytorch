import kagglehub

# Download latest version
path = kagglehub.dataset_download("amirmotefaker/detecting-fake-news-dataset")

print("Path to dataset files:", path)

import os
import pandas as pd

for file in os.listdir(path):
  print(file)

file_path = os.path.join(path,"news.csv")
df = pd.read_csv(file_path)
df.head()

import torch
import torch.nn as nn
import torch.optim as optim

from sklearn.preprocessing import LabelEncoder
le = LabelEncoder()
df['label'] = le.fit_transform(df['label'])
df.head()

from sklearn.feature_extraction.text import TfidfVectorizer
tfidf = TfidfVectorizer(max_features=10000,ngram_range=(1,2))
x = tfidf.fit_transform(df['text'].values).toarray()
y = df['label'].values

model = nn.Sequential(
    nn.Linear(10000,512),
    nn.ReLU(),
    nn.Linear(512,256),
    nn.ReLU(),
    nn.Linear(256,1),
    nn.Sigmoid()
)

optimizer = optim.Adam(model.parameters(),lr=0.001)

loss = nn.BCELoss()

epochs = 100
for epoch in range(epochs):
  prediction = model(torch.tensor(x,dtype=torch.float32))
  target = torch.tensor(y,dtype=torch.float32).view(-1, 1)
  current_loss = loss(prediction, target)
  optimizer.zero_grad()
  current_loss.backward()
  optimizer.step()
  print(f"Epoch :{epoch} , Loss :{current_loss.item()}")

df['label'].value_counts()

